<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				chanpinmingcheng: '',
				chanpinfenlei: '',
				chanpintupian: '',
				chanpinxiangqing: '',
				price: '',
				},
				chanpinfenleiOptions: [],
				chanpinfenleiIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   chanpinmingcheng : false,
                                   chanpinfenlei : false,
                                   chanpintupian : false,
                                   chanpinxiangqing : false,
                                   price : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

			// 下拉框
			res = await this.$api.option(`chanpinfenlei`,`chanpinfenlei`,{});
			this.chanpinfenleiOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xinlichanpin`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='chanpinmingcheng'){
					this.ruleForm.chanpinmingcheng = obj[o];
					this.ro.chanpinmingcheng = true;
					continue;
					}
					if(o=='chanpinfenlei'){
					this.ruleForm.chanpinfenlei = obj[o];
					this.ro.chanpinfenlei = true;
					continue;
					}
					if(o=='chanpintupian'){
					this.ruleForm.chanpintupian = obj[o];
					this.ro.chanpintupian = true;
					continue;
					}
					if(o=='chanpinxiangqing'){
					this.ruleForm.chanpinxiangqing = obj[o];
					this.ro.chanpinxiangqing = true;
					continue;
					}
					if(o=='price'){
					this.ruleForm.price = obj[o];
					this.ro.price = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数



			// 下拉变化
			chanpinfenleiChange(e) {
				this.chanpinfenleiIndex = e.target.value
				this.ruleForm.chanpinfenlei = this.chanpinfenleiOptions[this.chanpinfenleiIndex]
			},

			chanpintupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.chanpintupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if((!this.ruleForm.price)){
					this.$utils.msg(`价格不能为空`);
					return
				}
				if(this.ruleForm.price&&(!this.$validate.isNumber(this.ruleForm.price))){
					this.$utils.msg(`价格应输入数字`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`xinlichanpin`, this.ruleForm);
				}else{
					await this.$api.add(`xinlichanpin`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

